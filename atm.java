public class atm {
    private double viewBalance;
    private double WithdrawBalance;
    private double depositAmount;

    // default constructor
    public atm(){

    }

    public double getViewBalance() {
        return viewBalance;
    }

    public void setViewBalance(double viewBalance) {
        this.viewBalance = viewBalance;
    }

    public double getWithdrawBalance() {
        return WithdrawBalance;
    }

    public void setWithdrawBalance(double withdrawBalance) {
        WithdrawBalance = withdrawBalance;
    }

    public double getDepositAmount() {
        return depositAmount;
    }

    public void setDepositAmount(double depositAmount) {
        this.depositAmount = depositAmount;
    }

   
}
